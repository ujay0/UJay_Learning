# Write the Formulas for the Pearson Correlation Coefficient (r) and explain each component of the formula.

# The Pearson Correlation Coefficient (r) is a measure of the linear relationship between two variables.

# It is calculated using the following formula:
# r = (n * Σ(xy) - Σx * Σy) / sqrt((n * Σ(x^2) - (Σx)^2) * (n * Σ(y^2) - (Σy)^2))

# Where:
# n = number of data points
# Σ(xy) = sum of the product of paired scores
# Σx = sum of the x scores
# Σy = sum of the y scores
# Σ(x^2) = sum of the squares of the x scores
# Σ(y^2) = sum of the squares of the y scores